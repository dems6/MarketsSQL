  CREATE TABLE ZAKUPY 
   (
      ID_ZAKUPU 	NUMBER
	, ID_KLIENTA 	NUMBER 
	, CZY_PARAGON VARCHAR2(1) 
	, CZY_FAKTURA VARCHAR2(1)
	, ID_FAKTURY 	NUMBER
	, DATA_ZAKUPU DATE
	, PRIMARY KEY (ID_ZAKUPU)
	, FOREIGN KEY (ID_KLIENTA)REFERENCES KLIENCI (ID)
	, FOREIGN KEY (ID_FAKTURY) REFERENCES FAKTURY_SPRZEDAZY (ID_FAKTURY)
   ) ;

  CREATE TABLE ZAKUPY_POZYCJE 
   (
      ID_ZAKUPU 		NUMBER
	, NUMER_POZYCJI 	NUMBER 
	, ID_PRODUKTU 		NUMBER 
