 CREATE TABLE PRACOWNICY 
   (
      ID 		 		NUMBER 
	, IMIE 	 		    VARCHAR2(30) NOT NULL
	, NAZWISKO 		    VARCHAR2(60) NOT NULL
	, PESEL 	 		VARCHAR2(11) NOT NULL 
	, PLEC 			    VARCHAR2(1)  NOT NULL 
	, DATA_URODZENIA    DATE
	, PENSJA 			NUMBER 
	, KOD_DEPARTAMENTU  NUMBER
    , CHECK (plec in ('M', 'K')) 
    , CONSTRAINT PRACOWNICY_PK PRIMARY KEY (ID)
	);

  CREATE TABLE DEPARTAMENTY 
   (
     KOD_DEPARTAMENTU 	NUMBER
   , NAZWA_DEPARTAMENTU VARCHAR2(40) 
   , ID_KIEROWNIKA 		NUMBER 			NOT NULL 
   , MIEJSCOWOSC 		VARCHAR2(50)
   , CONSTRAINT PK_DEPARTAMENTY PRIMARY KEY (KOD_DEPARTAMENTU)
   , CONSTRAINT FK_DEP_PRACOWNIK FOREIGN KEY (ID_KIEROWNIKA)
	 REFERENCES PRACOWNICY (ID)
   );
   
   
	 CREATE TABLE TYPY_PRODUKTOW 
	(
	  TYP_PRODUKTU   VARCHAR2(20)
	, NAZWA_PELNA    VARCHAR2(40) NOT NULL
	, RABAT 		 NUMBER 
	, OKRES_WAZNOSCI VARCHAR2(1)
	, CHECK (okres_waznosci in ('K', 'D'))
	, PRIMARY KEY (TYP_PRODUKTU)
	);
	
 
  CREATE TABLE PRODUKTY 
   (
      ID 				NUMBER
	, NAZWA_PRODUKTU    VARCHAR2(100)
	, TYP_PRODUKTU 		VARCHAR2(30) 
	, CENA_SPRZEDAZY 	NUMBER 
	, CENA_ZAKUPU 		NUMBER
	, STAWKA_PODATKU 	NUMBER
	, JM 				VARCHAR2(10) 
	, CONSTRAINT PK_PRODUKTY PRIMARY KEY (ID)
	, CONSTRAINT FK_TYP_PRODUKTU FOREIGN KEY (TYP_PRODUKTU)
	  REFERENCES TYPY_PRODUKTOW (TYP_PRODUKTU) ENABLE
   )
   ;

  CREATE TABLE RODZAJE_SRODKOW_TRANSPORTU 
   (  KOD 			VARCHAR2(10) 
	, PELNA_NAZWA 	VARCHAR2(80) NOT NULL ENABLE
	, MIN_DLUGOSC 	NUMBER
	, MAX_DLUGOSC 	NUMBER
	, MIN_WAGA 		NUMBER
	, MAX_WAGA 		NUMBER
	, LADOWNOSC 	NUMBER
	, PRIMARY KEY (KOD)
  );

  CREATE TABLE DOSTAWCY 
   (
      ID_DOSTAWCY 					    NUMBER
	, NAZWA_DOSTAWCY 					VARCHAR2(50) NOT NULL 
	, KOD_RODZAJU_SRODKA_TRANSPORTU 	VARCHAR2(10)
	, STATUS_AKTYWNOSCI 				VARCHAR2(1)
	, PRIMARY KEY (ID_DOSTAWCY)
	, FOREIGN KEY (KOD_RODZAJU_SRODKA_TRANSPORTU)
	  REFERENCES RODZAJE_SRODKOW_TRANSPORTU (KOD)
   );


  CREATE TABLE PRZYCZYNY_ABSENCJI 
   (
      ID 		NUMBER 
	, NAZWA 	VARCHAR2(200) 	NOT NULL
	, CZY_ZGODA VARCHAR2(1) 	DEFAULT 'N'
	, UWAGI 	VARCHAR2(1000)
	, PRIMARY KEY (ID)
  );

  CREATE TABLE ABSENCJE 
   (
      ID 			  NUMBER
	, ID_PRACOWNIKA   NUMBER
	, PA_KOD 		  NUMBER
	, DATA_OD 	  	  DATE
	, DATA_DO         DATE
	, PRIMARY KEY (ID)
	, FOREIGN KEY (ID_PRACOWNIKA) REFERENCES PRACOWNICY (ID)
    , FOREIGN KEY (PA_KOD) REFERENCES PRZYCZYNY_ABSENCJI (ID)
   );

  CREATE TABLE KLIENCI 
   (  ID 		     NUMBER
	, IMIE 	         VARCHAR2(30) NOT NULL
	, NAZWISKO  	 VARCHAR2(60) NOT NULL
	, PESEL 		 VARCHAR2(11) 
	, KOD_POCZTOWY   VARCHAR2(10)
	, MIEJSCOWOSC    VARCHAR2(40)
	, PRIMARY KEY (ID)
  );

  CREATE TABLE FAKTURY_SPRZEDAZY 
   (
      ID_FAKTURY 		NUMBER
	, DATA_FAKTURY    	DATE 
	, ID_KLIENTA 		NUMBER 
	, WARTOSC_NETTO 	NUMBER 
	, WARTOSC_BRUTTO 	NUMBER 
	, STAWKA_PODATKU 	NUMBER 
	, WALUTA 			VARCHAR2(4) 
